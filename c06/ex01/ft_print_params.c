#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;

	i = 1;
	if ((argc - 1) > 0)
	{
		while (i <= (argc - 1))
		{
			if (i != 1)
				write(1, "\n", 1);
			while (*argv[i] != '\0')
			{
				write(1, argv[i], 1);
				argv[i]++;
			}
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
